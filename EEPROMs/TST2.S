INCLUDE .\BIN\BOOTLD.SYM


NUMBUFF EQU $F0B7


VIDADDR EQU $A000  ;BANK 5
VIDCNFG EQU $BFF8 ;32760

TSTN EQU $C0
TSTB EQU 5

INITVGA EQU $0059

ORG $E000


LD A,$C3
LD B,2
CALL PUTPGINBANK
LD A,($5FF0)
CALL PRINTNUM
LD A,($5FF1)
CALL PRINTNUM


JP BOOTMENU


CALL VGA_GETCONF 						;GET CURRENT MODE AND COLORS
LD A,C
CALL PRINTNUM
LD A,B
CALL PRINTNUM
JP BOOTMENU


CALL VGA_INIT
CALL VGA_SETLOWTX

CALL VGA_GETCONF 						;GET CURRENT MODE AND COLORS
LD A,C
CALL PRINTNUM
LD A,B
CALL PRINTNUM


SKP1:

CALL RS_MESG
DEFM 'WRITE TO PAGES X0 TO X3 '
     DB 10,13,0


LD A,TSTN 
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,$C0
LD (HL),A

LD A,TSTN+1
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2
LD A,$C1
LD (HL),A

LD A,TSTN+2
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,$C2
LD (HL),A

LD A,TSTN+3
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,$C3
LD (HL),A

JP SKP2

CALL RS_MESG
DEFM 'READ 0,1,2'
     DB 10,13,0

LD HL,VIDCNFG 
LD A, (HL)
LD B,A
INC HL
LD A, (HL)
PUSH BC
CALL PRINTNUM
POP BC
LD A,B
CALL PRINTNUM
INC HL
LD A, (HL)
CALL PRINTNUM

SKP2:
CALL RS_MESG
DEFM 'READ PAGES X0 TO X3 '
     DB 10,13,0


LD A,TSTN
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,(HL)
CALL PRINTNUM

LD A,TSTN+1
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,(HL)
CALL PRINTNUM

LD A,TSTN+2
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,(HL)
CALL PRINTNUM

LD A,TSTN+3
LD B,TSTB 
CALL PUTPGINBANK
LD HL,VIDCNFG+2 
LD A,(HL)
CALL PRINTNUM




JP BOOTMENU

TMP	DEFB 0,0

PUTMESS: LD A,(HL)
	CP 0
	RET Z
	LD (DE),A
	INC DE
	INC HL
	JR PUTMESS

MESS1: DEFM "HELLO WORLD"
       DB 0

MESS2: DEFM "WORLD HELLO"
       DB 0
